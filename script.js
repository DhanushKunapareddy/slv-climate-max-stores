// Function to populate the table with store data
function populateTable(data) {
    const tableBody = document.querySelector("#storesTable tbody");
    tableBody.innerHTML = ""; // Clear existing rows

    data.forEach(store => {
        const row = document.createElement("tr");
        row.innerHTML = `
            <td>${store.Name}</td>
            <td>${store.Address}</td>
            <td>${store.Phone}</td>
            <td><a href="${store.Map_link}" target="_blank">View Map</a></td>
        `;
        tableBody.appendChild(row);
    });
}

// Function to filter stores based on search input
function searchStores() {
    const input = document.getElementById("searchInput").value.toLowerCase();
    const filteredData = storeData.filter(store => 
        store.Name.toLowerCase().includes(input) || 
        store.Address.toLowerCase().includes(input)
    );
    populateTable(filteredData);
}

